DROP DATABASE cursac;
CREATE DATABASE cursac;
USE cursac;

  
CREATE TABLE curso
  (
    codigo      int(11) NOT NULL ,
    nombre      varchar (50) NOT NULL ,
    creditos    int(11) ,
    obligatorio int(11) NOT NULL,
	PRIMARY KEY(codigo)
  ) ;

CREATE TABLE curso_pre_post
  (
    padre int(11) NOT NULL ,
    hijo  int(11) NOT NULL ,
	PRIMARY KEY( padre, hijo ),
	CONSTRAINT curso_pre_post_curso_FK FOREIGN KEY ( padre ) REFERENCES curso ( codigo ) ,
	CONSTRAINT curso_pre_post_curso_FKv1 FOREIGN KEY ( hijo ) REFERENCES curso ( codigo )
  ) ;

  
CREATE TABLE dia
  ( 
    id_dia int(11) NOT NULL , 
    nombre varchar(10) NOT NULL,
	PRIMARY KEY(id_dia)
  ) ;

CREATE TABLE edificio
  (
    id_edificio int(11) NOT NULL ,
    nombre      varchar (30) NOT NULL,
	PRIMARY KEY( id_edificio )
  ) ;

CREATE TABLE horario
  (
    id_horario int(11) NOT NULL ,
    horaInicio TIME NOT NULL ,
    horaFin    TIME NOT NULL ,
	PRIMARY KEY( id_horario)
  ) ;

CREATE TABLE periodo
  ( 
   id_periodo int(11) NOT NULL,
   semestre varchar(25) NOT NULL , 
   año int(11) NOT NULL ,
   PRIMARY KEY( id_periodo )
  ) ;


CREATE TABLE salon
  (
    id_salon int(11) NOT NULL ,
    nombre   varchar (15) NOT NULL,
	id_edificio int(11) NOT NULL,
	PRIMARY KEY( id_salon ),
	CONSTRAINT salon_edificio_FK FOREIGN KEY ( id_edificio ) REFERENCES edificio ( id_edificio )
  ) ;

CREATE TABLE seccion
  ( 
  id_seccion int(11) NOT NULL , 
  nombre varchar (10),
  PRIMARY KEY(id_seccion)
  ) ;
  
  CREATE TABLE asignacion_horario
  (
    id_seccion  int(11) NOT NULL ,
    id_periodo  int(11) NOT NULL ,
    id_dia      int(11) NOT NULL ,
    id_salon    int(11) NOT NULL ,
    id_horario  int(11) NOT NULL ,
    codigo      int(11) NOT NULL ,
	PRIMARY KEY( id_seccion, id_periodo, id_dia, id_salon, id_horario, codigo),
	CONSTRAINT asignacion_horario_curso_FK FOREIGN KEY ( codigo ) REFERENCES curso ( codigo ),
	CONSTRAINT asignacion_horario_dia_FK FOREIGN KEY ( id_dia ) REFERENCES dia ( id_dia ),
	CONSTRAINT asignacion_horario_horario_FK FOREIGN KEY ( id_horario ) REFERENCES horario ( id_horario ),
	CONSTRAINT asignacion_horario_periodo_FK FOREIGN KEY ( id_periodo ) REFERENCES periodo ( id_periodo ) ,
	CONSTRAINT asignacion_horario_salon_FK FOREIGN KEY ( id_salon ) REFERENCES salon ( id_salon ) ,
	CONSTRAINT asignacion_horario_seccion_FK FOREIGN KEY ( id_seccion ) REFERENCES seccion ( id_seccion )
  ) ;

  
